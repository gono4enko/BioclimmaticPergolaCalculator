def get_pergola_images(pergola_type):
    """
    Возвращает список путей к изображениям для указанного типа перголы
    
    Args:
        pergola_type (str): Тип перголы (B500NEW, B700NEW, B600)
        
    Returns:
        list: Список путей к изображениям
    """
    # Словарь с изображениями для каждого типа перголы
    images = {
        "B500NEW": [
            "attached_assets/b500_rotation.png",
            "attached_assets/aluminum_slats.png"
        ],
        "B700NEW": [
            "attached_assets/b700_sliding.png",
            "attached_assets/aluminum_slats.png"
        ],
        "B600": [
            "attached_assets/b600_sandwich.png"
        ]
    }
    
    # Общие изображения для всех типов пергол
    common_images = [
        "attached_assets/install_design_system.png"
    ]
    
    # Объединяем специфические и общие изображения
    result = images.get(pergola_type, []) + common_images
    
    return result

def get_pergola_image_caption(image_path):
    """
    Возвращает подпись для изображения
    
    Args:
        image_path (str): Путь к изображению
        
    Returns:
        str: Подпись к изображению
    """
    # Словарь подписей к изображениям
    captions = {
        "attached_assets/b500_rotation.png": "Пергола В500 с поворотными ламелями",
        "attached_assets/b700_sliding.png": "Пергола В700 с поворотно-сдвижными ламелями",
        "attached_assets/b600_sandwich.png": "Пергола В600 с PIR сэндвич-панелями",
        "attached_assets/aluminum_slats.png": "Алюминиевые ламели для пергол",
        "attached_assets/install_design_system.png": "Система проектирования и монтажа"
    }
    
    # Получаем только имя файла из пути
    filename = image_path.split("/")[-1]
    full_path = f"attached_assets/{filename}"
    
    # Возвращаем подпись или None если подпись не найдена
    return captions.get(full_path, None)

