def get_pergola_images(pergola_type):
    """
    Возвращает список путей к изображениям для указанного типа перголы
    
    Args:
        pergola_type (str): Тип перголы (B500NEW, B700NEW, B600, MODULAR, DRAINAGE, SOMFY, BANSBACH и т.д.)
        
    Returns:
        list: Список путей к изображениям
    """
    # Словарь с шаблонами поиска изображений по типу перголы
    image_patterns = {
        "B500NEW": [
            "attached_assets/*B500*.png", 
            "attached_assets/*500*.png",
            "attached_assets/*вращен*.png",
            "attached_assets/Снимок*B500*.png",
            "attached_assets/Снимок*23.20.06.png",
        ],
        
        "B700NEW": [
            "attached_assets/*B700*.png", 
            "attached_assets/*700*.png",
            "attached_assets/*сдвиж*.png",
            "attached_assets/Снимок*B700*.png",
            "attached_assets/Снимок*23.11.44.png",
        ],
        
        "B600": [
            "attached_assets/*B600*.png", 
            "attached_assets/*600*.png",
            "attached_assets/*сэндвич*.png",
            "attached_assets/Снимок*B600*.png",
            "attached_assets/Снимок*23.02.37.png",
        ],
        
        "MODULAR": [
            "attached_assets/*модул*.png",
            "attached_assets/*modul*.png",
            "attached_assets/install_design_system.png",
            "attached_assets/Instal and design Sistem.png",
            "attached_assets/Модульная*система*.png",
            "attached_assets/Снимок*система*.png",
        ],
        
        "DRAINAGE": [
            "attached_assets/*водоотв*.png",
            "attached_assets/*drain*.png",
            "attached_assets/Система*водоотведения*.png",
            "attached_assets/Снимок*водоотв*.png",
        ],
        
        "SOMFY": [
            "attached_assets/*Somfy*.png",
            "attached_assets/*Somfy*.jpeg",
            "attached_assets/Снимок*Somfy*.png",
        ],
        
        "BANSBACH": [
            "attached_assets/*Линейный*привод*.png",
            "attached_assets/*Линейный*привод*.jpeg",
            "attached_assets/*lineardrive*.png",
            "attached_assets/*lineardrive*.jpeg",
            "attached_assets/Lin gate.jpg",
        ],
    }
    
    # Список для хранения найденных изображений
    found_images = []
    
    # Если тип перголы есть в словаре шаблонов
    if pergola_type in image_patterns:
        # Перебираем все шаблоны для данного типа
        for pattern in image_patterns[pergola_type]:
            # Ищем файлы по шаблону
            matching_files = glob.glob(pattern)
            # Добавляем найденные файлы в список
            found_images.extend(matching_files)
    
    # Удаляем дубликаты
    found_images = list(set(found_images))
    
    return found_images


