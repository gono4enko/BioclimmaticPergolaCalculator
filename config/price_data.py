# Функция определения цены опции
def get_option_price(option_name, width_mm=0, length_mm=0):
    """
    Возвращает цену опции в зависимости от названия и, возможно, размеров перголы
    
    Args:
        option_name (str): Название опции
        width_mm (int, optional): Ширина перголы в мм
        length_mm (int, optional): Длина перголы в мм
        
    Returns:
        float: Цена опции
    """
    if option_name in ADDITIONAL_OPTIONS_PRICES:
        price = ADDITIONAL_OPTIONS_PRICES[option_name]
        
        # Если цена задана функцией, вычисляем ее с учетом размеров
        if callable(price):
            return price(width_mm, length_mm)
        
        return price
    
    return 0

