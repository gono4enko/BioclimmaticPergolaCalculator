def get_variant_options(pergola_type):
    if pergola_type not in VARIANT_DISPLAY_ORDER:
        return []
    result = []
    specs = VARIANT_SPECS.get(pergola_type, {})
    for item in VARIANT_DISPLAY_ORDER[pergola_type]:
        variant = item["variant"]
        ls = item["lamella_size"]
        spec = specs.get(variant, {}).get(ls, {})
        result.append({
            "variant": variant,
            "lamella_size": ls,
            "label": item["label"],
            "lamella": spec.get("lamella", ""),
            "column": spec.get("column", ""),
            "beam": spec.get("beam", ""),
            "beam_double": spec.get("beam_double", ""),
            "max_overhang": spec.get("max_overhang", 0),
            "max_module_width": spec.get("max_module_width", 0),
            "max_structure_size": spec.get("max_structure_size", ""),
            "weight": spec.get("weight", ""),
            "snow_wind_load": spec.get("snow_wind_load", ""),
            "hermiticity": spec.get("hermiticity", ""),
            "heat_protection": spec.get("heat_protection", ""),
            "parking_zone": spec.get("parking_zone", ""),
            "rotation_type": spec.get("rotation_type", ""),
            "rotation_angle": spec.get("rotation_angle", ""),
            "opening_percent": spec.get("opening_percent", ""),
            "frame_rigidity": spec.get("frame_rigidity", ""),
            "images": spec.get("images", {}),
            "pros": spec.get("pros", []),
            "cons": spec.get("cons", []),
        })
    return result
